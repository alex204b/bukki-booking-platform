interface I18nContextType {
  lang: Lang;
  t: (key: keyof Messages) => string;
  setLang: (l: Lang) => void;
}

const I18nContext = createContext<I18nContextType | undefined>(undefined);

// Safe localStorage helper functions
const safeGetLang = (): Lang => {
  try {
    if (typeof localStorage === 'undefined') {
      console.warn('[I18nContext] localStorage not available, using default language');
      return 'ro';
    }
    const stored = localStorage.getItem('lang');
    if (stored && (stored === 'ro' || stored === 'en' || stored === 'ru')) {
      return stored as Lang;
    }
    return 'ro';
  } catch (error) {
    console.error('[I18nContext] Error reading language from localStorage:', error);
    return 'ro';
  }
};

const safeSetLang = (lang: Lang): void => {
  try {
    if (typeof localStorage === 'undefined') {
      console.warn('[I18nContext] localStorage not available, cannot save language preference');
      return;
    }
    localStorage.setItem('lang', lang);
  } catch (error) {
    console.error('[I18nContext] Error saving language to localStorage:', error);
    // Don't throw - just log the error and continue
  }
};

export const I18nProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [lang, setLangState] = useState<Lang>(safeGetLang);

  useEffect(() => {
    safeSetLang(lang);
  }, [lang]);

  const t = (key: keyof Messages) => {
    try {
      return bundles[lang][key] || key;
    } catch (error) {
      console.error('[I18nContext] Error translating key:', key, error);
      return key;
    }
  };
  
  const setLang = (l: Lang) => {
    try {
      if (l === 'ro' || l === 'en' || l === 'ru') {
        setLangState(l);
      } else {
        console.warn('[I18nContext] Invalid language:', l);
      }
    } catch (error) {
      console.error('[I18nContext] Error setting language:', error);
    }
  };

  return (
    <I18nContext.Provider value={{ lang, t, setLang }}>
      {children}
    </I18nContext.Provider>
  );
};

export const useI18n = () => {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error('useI18n must be used within I18nProvider');
  return ctx;
};


